package pt.ipleiria.estg.dei.musicaev1.modelos;

public class Industria {

    private int idIndustria;
    private String tipo;

    public Industria(String tipo){
        this.idIndustria = idIndustria;
        this.tipo = tipo;
    }

    public int getIdIndustria() {
        return idIndustria;
    }

    public String getTipo() {
        return tipo;
    }
}
